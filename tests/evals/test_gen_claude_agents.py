"""EVALS §3 — Config Generation: Claude Agents."""

from __future__ import annotations
from pathlib import Path

from subagent_fleet.config import load_config
from subagent_fleet.generators.claude_agents import generate_claude_agents


FLEET = """project: {name: cluster}
nodes:
  laptop:     {endpoint: http://localhost:11434, tags: [local]}
  mac64:      {endpoint: http://192.168.40.69:11434, tags: [heavy, coder]}
models:
  heavy-coder:       {node: mac64, ollama_model: qwen3-coder-next:latest, litellm_alias: sonnet-local}
  small-planner: {node: laptop, ollama_model: gemma4:latest, litellm_alias: haiku-local}
agents:
  planner:     {model: small-planner, description: Use for planning.}
  implementer: {model: heavy-coder, description: Use for implementation.}"""


def test_planner_description(tmp_path: Path) -> None:
    p = tmp_path / "fleet.yaml"
    p.write_text(FLEET)
    cfg = load_config(p)
    out = generate_claude_agents(cfg, tmp_path / "agents")
    content = (tmp_path / "agents" / "planner.md").read_text()
    assert "planning" in content


